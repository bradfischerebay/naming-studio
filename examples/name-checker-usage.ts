// Example 11: Pre-flight Validation in Form
// ============================================================================

async function formValidationExample() {
  // In your form submission handler
  const proposedName = 'UserInputName';

  // Quick check before full evaluation
  const response = await fetch('/api/check-names', {
    method: 'POST',
    body: JSON.stringify({ names: proposedName })
  });

  const data = await response.json();
  const result = data.results[0];

  if (result.riskLevel === 'critical' || result.riskLevel === 'high') {
    const conflictSummary = result.conflicts
      .map((c: any) => c.description)
      .join('\n');

    const proceed = confirm(
      `Warning: This name has conflicts:\n\n${conflictSummary}\n\nDo you want to proceed anyway?`
    );

    if (!proceed) {
      return; // Cancel submission
    }
  }

  // Continue with full evaluation
  console.log('Proceeding with evaluation...');
}

