// Example 12: Integration with Existing Evaluate Endpoint
// ============================================================================

async function evaluateWithNameCheck(brief: string, model?: string) {
  // Step 1: Run name check (optional pre-flight)
  const g4Enhancement = await augmentG4WithNameCheck(brief);

  if (g4Enhancement && g4Enhancement.status === 'Fail') {
    console.warn('Name will likely fail G4 gate');
  }

  // Step 2: Run normal evaluation
  const response = await fetch('/api/evaluate', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ brief, model })
  });

  const evaluationResult = await response.json();

  // Step 3: Optionally override G4 with name check data
  if (g4Enhancement && g4Enhancement.autoGenerated) {
    evaluationResult.gatekeeperResult.G4 = {
      status: g4Enhancement.status,
      reasoning: g4Enhancement.reasoning
    };
  }

  return evaluationResult;
}

