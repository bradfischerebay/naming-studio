async function basicNameCheck() {
  const nameChecker = getNameChecker();

  const result = await nameChecker.checkName('eBay SmartSell');

  console.log('Name:', result.name);
  console.log('Available:', result.isAvailable);
  console.log('Risk Level:', result.riskLevel);
  console.log('Conflicts:', result.conflicts.length);
  console.log('Recommendations:', result.recommendations);
}

// ============================================================================
// Example 2: Custom Options
// ============================================================================

async function customOptionsCheck() {
  const nameChecker = getNameChecker();

  const options: NameCheckOptions = {
    checkEbay: true,           // Search eBay marketplace
    checkTrademarks: true,     // Check USPTO database
    maxEbayResults: 50,        // Get more results
    useCache: true,            // Use caching
    includeRelatedTerms: false // Don't check variations
  };

  const result = await nameChecker.checkName('QuickList Pro', options);

  // Access detailed data
  if (result.ebayData) {
    console.log('Total eBay products:', result.ebayData.totalProducts);
    console.log('Sample products:', result.ebayData.sampleProducts);
    console.log('Search URL:', result.ebayData.searchUrl);
  }

  if (result.trademarkData) {
    console.log('Trademark conflicts:', result.trademarkData.conflicts.length);
    console.log('Trademark risk:', result.trademarkData.riskLevel);
  }

  // Analyze conflicts
  result.conflicts.forEach(conflict => {
    console.log(`${conflict.type} conflict:`, conflict.description);
    console.log(`Severity: ${conflict.severity}`);
  });
}

// ============================================================================
// Example 3: Batch Name Checking
// ============================================================================

async function batchNameCheck() {
  const nameChecker = getNameChecker();

  const candidates = [
    'AutoSell Plus',
    'SmartList Manager',
    'QuickPost Pro',
    'eBay Turbo' // This should fail due to portfolio conflict
  ];

  const results = await nameChecker.batchCheckNames(candidates);

  // Sort by risk level
  const sorted = results.sort((a, b) => {
    const levels = { low: 0, medium: 1, high: 2, critical: 3 };
    return levels[a.riskLevel] - levels[b.riskLevel];
  });

  console.log('Best Options (lowest risk first):');
  sorted.forEach(result => {
    console.log(`- ${result.name}: ${result.riskLevel} risk`);
  });
}

// ============================================================================
// Example 4: G4 Gate Integration - Single Name
// ============================================================================

async function g4SingleEnhancement() {
  const proposedName = 'SellerTools Hub';

  const g4Result = await enhanceG4Evaluation(proposedName);

  console.log('G4 Status:', g4Result.status); // Pass, Fail, Pending, Unknown
  console.log('G4 Reasoning:', g4Result.reasoning);
  console.log('Auto-generated:', g4Result.autoGenerated);

  // Access underlying name check data
  if (g4Result.nameCheckData) {
    console.log('Conflicts:', g4Result.nameCheckData.conflicts);
  }

  // Use in evaluation flow
  if (g4Result.status === 'Fail') {
    console.log('❌ G4 Gate Failed - Choose different name');
  } else if (g4Result.status === 'Pending') {
    console.log('⚠️ G4 Gate Pending - Manual review needed');
  } else {
    console.log('✅ G4 Gate Passed');
  }
}

// ============================================================================
// Example 5: G4 Gate Integration - Batch Comparison
// ============================================================================

async function g4BatchComparison() {
  const candidates = [
    'AutoList Pro',
    'Seller Companion',
    'QuickPost Manager'
  ];

  const results = await enhanceG4BatchEvaluation(candidates);

  // Generate summary
  const summary = generateG4Summary(results);
  console.log(summary);

  // Detailed analysis
  results.forEach((g4Result, name) => {
    console.log(`\n${name}:`);
    console.log(`  Status: ${g4Result.status}`);
    console.log(`  Reasoning: ${g4Result.reasoning.substring(0, 100)}...`);

    if (g4Result.nameCheckData) {
      console.log(`  Risk: ${g4Result.nameCheckData.riskLevel}`);
      console.log(`  Conflicts: ${g4Result.nameCheckData.conflicts.length}`);
    }
  });

  // Find best option
  const passedNames = Array.from(results.entries())
    .filter(([_, result]) => result.status === 'Pass')
    .map(([name]) => name);

  console.log('\nRecommended names:', passedNames);
}

